dia = int (input("Digite um número de 1 a 7, para saber o dia da semana: " ))
if dia == 1:
    print("O dia correspondente ao número é Domingo! ")

elif dia == 2:
    print("O dia correspondente ao número é Segunda-feira! ")    

elif dia == 3:
    print("O dia correspondente ao número é Terça-feira! ")

elif dia == 4:
    print("O dia correspondente ao número é Quarta-feira! ")

elif dia == 5:
    print("O dia correspondente ao número é Quinta-feira! ")

elif dia == 6:
    print("O dia correspondente ao número é Sexta-feira! ")

elif dia == 7:
    print("O dia correspondente ao número é Sábado! ")

else:
    print("Número incorreto!")
    
